package package1;

public class DynamicCalculator 
{
	
	public static String getMyFullName(String first,String lastname)
	{
		String fullname = first+" "+lastname;
		return fullname;
	}
	
public int add(int a,int b)
{
	int c=a+b;
	return c;
}
public double sub(double x,double y)
{
	double z=x-y;
	return z;
}
}
